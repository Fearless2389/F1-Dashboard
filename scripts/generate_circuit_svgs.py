def main() -> None:
    circuits = pd.read_csv(CIRCUITS_FILE)
    written = 0
    for _, row in circuits.iterrows():
        cid = row["circuit_id"]
        name = row.get("name") or cid
        path_d = PATHS.get(cid)
        if not path_d:
            # Fallback: stylised ellipse for circuits without a tuned path
            path_d = "M 0.10 0.50 C 0.10 0.28 0.32 0.18 0.50 0.20 C 0.70 0.22 0.88 0.34 0.88 0.50 C 0.88 0.68 0.68 0.78 0.50 0.78 C 0.30 0.78 0.10 0.70 0.10 0.50 Z"
        svg = SVG_TEMPLATE.format(path=path_d, label=str(name).upper())
        out = OUTPUT_DIR / f"{cid}.svg"
        out.write_text(svg, encoding="utf-8")
        written += 1
    print(f"Wrote {written} circuit SVGs to {OUTPUT_DIR}")


if __name__ == "__main__":
    main()
